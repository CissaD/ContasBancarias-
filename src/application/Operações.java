package application;

import java.sql.Date;

public class Operações {
	
	int Nunconta;
	int operações;
	double valor;
	Date data;
	
	
	public int getOperações() {
		return operações;
	}
	public void setOperações(int operações) {
		this.operações = operações;
	}
	public double getValor() {
		return valor;
	}
	public void setValor(double valor) {
		this.valor = valor;
	}
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
	public int getNunconta() {
		return Nunconta;
	}

	
}
